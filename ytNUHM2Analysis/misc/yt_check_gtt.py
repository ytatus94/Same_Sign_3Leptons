#!/usr/bin/python
import os, ROOT

f1=ROOT.TFile("/Users/ytshen/Desktop/skim/Results/20170118/hist-RLE-merged-data-elec.root")
f2=ROOT.TFile("/Users/ytshen/Desktop/skim/Results/20170118/RLE_MC_GG_ttn1_electron/hist-20170112.root")
h1b=f1.Get("h_baseline_eta")
h1s=f1.Get("h_signal_eta")
h2b=f2.Get("h_baseline_eta")
h2s=f2.Get("h_signal_eta")
h1b.SetLineColor(2)
h1s.SetLineColor(4)
h2b.SetLineColor(2)
h2s.SetLineColor(4)
h2b.Scale(-1)
h2s.Scale(-1)
c1=ROOT.TCanvas("c1","c1",1000,500)
c1.Divide(2,1)
c1.cd(1)
h1b.SetStats(0)
h1b.Draw("hist")
h1s.Draw("hist,same")
leg1=ROOT.TLegend(0.5,0.7,0.7,0.8)
leg1.SetHeader("Data (electron)","c")
leg1.AddEntry(h1b,"baseline","l")
leg1.AddEntry(h1s,"signal","l")
leg1.Draw("same")
c1.cd(2)
h2b.SetStats(0)
h2b.Draw("hist")
h2s.Draw("hist,same")
leg2=ROOT.TLegend(0.5,0.7,0.7,0.8)
leg2.SetHeader("Gtt (electron)","c")
leg2.AddEntry(h2b,"baseline","l")
leg2.AddEntry(h2s,"signal","l")
leg2.Draw("same")
c1.SaveAs("c1.pdf")

